def get_term(english: str, language: str) -> str:
    """Translate a medical term to target language.

    Falls back to English if term or language not in glossary.
    """
    term_entry = GLOSSARY.get(english)
    if not term_entry:
        return english
    return term_entry.get(language, english)


def get_all_terms(language: str) -> dict:
    """Return all terms translated to target language."""
    return {eng: trans.get(language, eng) for eng, trans in GLOSSARY.items()}


def translate_text_with_glossary(text: str, language: str) -> str:
    """Simple term-by-term substitution for template-based offline reports.

    Not a replacement for LLM generation — used only for air-gapped fallback.
    """
    for eng_term, translations in GLOSSARY.items():
        local_term = translations.get(language)
        if local_term and eng_term.lower() in text.lower():
            text = text.replace(eng_term, f"{local_term} ({eng_term})")
    return text
